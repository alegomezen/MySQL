SELECT AVG(PUNTOS_POR_PARTIDO)
FROM ESTADISTICA AS E, EQUIPO AS EQ
WHERE EQ.DIVISION='PACIFIC';

SELECT MAX(PUNTOS_LOCAL-PUNTOS_VISITANTE)
FROM PARTIDO;

SELECT AVG((PUNTOS_LOCAL+PUNTOS_VISITANTE))
FROM PARTIDO AS P, EQUIPO AS E
WHERE E.DIVISION='PACIFIC';

